public class DataTypes {
    public static void main(String[] a){
        int i=100;
        float f=10.5f;
        double d=99.99;
        char c='A';
        boolean b=true;
        System.out.println("Integer (int) : "+i);
        System.out.println("Floating Point (float) : "+f);
        System.out.println("Double (double) : "+d);
        System.out.println("Character (char) : "+c);
        System.out.println("Boolean Value (boolean) : "+b);
    }
}